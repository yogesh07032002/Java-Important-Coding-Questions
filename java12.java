public class java12 {// operators
    public static void main(String[] args) {
        int a = 10;
        int b = ++a;// preincrement =first value change and then use.
        System.out.println(a);
        System.out.println(b);

        int c = 10;
        int d = c++;// post incement =value use and then value change.
        System.out.println(c);
        System.out.println(d);
    }

}
